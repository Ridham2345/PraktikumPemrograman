# volume kubus
sisi = 5 #panjang sisi kubus 
volume = sisi**3 # rumus volume kubus 
print("volume kubus dengan sisi", sisi,"adalah", volume)

#volume balok
panjang = 5 # panjang balok 
lebar = 3 # lebar balok
tinggi = 2 # tinggi balok
volume = panjang * lebar * tinggi #rumus volume balok
print("volume balok dengan panjang", panjang,",lebar", lebar,", dan  tinggi",tinggi,"adalah",volume)

